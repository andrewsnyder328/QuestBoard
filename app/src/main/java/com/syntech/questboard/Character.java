package com.syntech.questboard;

public class Character {

    private int id;
    private String name;
    private String image;
    private String profession;
    private String bio;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getProfession() {
        return profession;
    }

    public String getBio() {
        return bio;
    }
}
